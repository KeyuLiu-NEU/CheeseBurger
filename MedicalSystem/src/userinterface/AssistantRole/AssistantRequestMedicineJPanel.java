    private void requestMedicineJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_requestMedicineJButtonActionPerformed
        
        String message = messageJTextField.getText();
        
        AssistantMedicineRequest request = new AssistantMedicineRequest();
        request.setMessage(message);
        request.setSender(userAccount);
        request.setStatus("Sent");
        
        
        //userAccount.getWorkQueue().getWorkRequestList().add(request);
        network.getWorkQueue().getWorkRequestList().add(request);
        System.out.println("Add request success!");
       
        
        for(Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()){
            if(organization instanceof WarehouseOrganization){
                int currentMedicineANum = ((WarehouseOrganization) organization).getMedicineANum() - Integer.parseInt(medicineAJComboBox.getSelectedItem().toString());
                ((WarehouseOrganization) organization).setMedicineANum(currentMedicineANum);
                
                int currentMedicineBNum = ((WarehouseOrganization) organization).getMedicineBNum() - Integer.parseInt(medicineBJComboBox.getSelectedItem().toString());
                ((WarehouseOrganization) organization).setMedicineBNum(currentMedicineBNum);
                
                int currentMedicineCNum = ((WarehouseOrganization) organization).getMedicineCNum() - Integer.parseInt(medicineCJComboBox.getSelectedItem().toString());
                ((WarehouseOrganization) organization).setMedicineCNum(currentMedicineCNum);
            }
        }
        
        populateComboBox();
    }//GEN-LAST:event_requestMedicineJButtonActionPerformed

