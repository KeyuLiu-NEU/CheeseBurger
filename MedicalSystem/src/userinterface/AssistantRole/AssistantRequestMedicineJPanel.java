    private void populateComboBox() {
        medicineAJComboBox.removeAllItems();
        medicineBJComboBox.removeAllItems();
        medicineCJComboBox.removeAllItems();

        int medicineANum = 0;
        int medicineBNum = 0;
        int medicineCNum = 0;
        
        for(Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()){
            if(organization instanceof WarehouseOrganization){
                medicineANum = ((WarehouseOrganization) organization).getMedicineANum();
                medicineBNum = ((WarehouseOrganization) organization).getMedicineBNum();
                medicineCNum = ((WarehouseOrganization) organization).getMedicineCNum();
            }
                
        }
        
        for(int i = 0;i <= medicineANum;i++)
            medicineAJComboBox.addItem(i+"");
        for(int i = 0;i <= medicineBNum;i++)
            medicineBJComboBox.addItem(i+"");
        for(int i = 0;i <= medicineCNum;i++)
            medicineCJComboBox.addItem(i+"");

    }
