    private void requestMedicalDeviceJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_requestMedicalDeviceJButtonActionPerformed
        
        String message = messageJTextField.getText();
        int deviceANumRequested = Integer.parseInt(deviceAJComboBox.getSelectedItem().toString());
        int deviceBNumRequested = Integer.parseInt(deviceBJComboBox.getSelectedItem().toString());
        
        
        MedicalDeviceWorkRequest request = new MedicalDeviceWorkRequest();
        request.setMessage(message);
        request.setSender(userAccount);
        request.setStatus("Sent");
        request.setMedicalDeviceANum(deviceANumRequested);
        request.setMedicalDeviceBNum(deviceBNumRequested);

        //userAccount.getWorkQueue().getWorkRequestList().add(request);
        network.getWorkQueue().getWorkRequestList().add(request);
        System.out.println("Add " + request.getType() + "request success!");
        
        this.network.setMedicalDeviceA(this.network.getMedicalDeviceA() - deviceANumRequested);
        this.network.setMedicalDeviceB(this.network.getMedicalDeviceB() - deviceBNumRequested);
    }//GEN-LAST:event_requestMedicalDeviceJButtonActionPerformed

