    private void populateComboBox() {
        deviceAJComboBox.removeAllItems();
        deviceBJComboBox.removeAllItems();
        

        int deviceANum = 0;
        int deviceBNum = 0;
        
        
        for(Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()){
            if(organization instanceof WarehouseOrganization){
                deviceANum = network.getMedicalDeviceA();
                deviceBNum = network.getMedicalDeviceB();
                
            }
                
        }
        
        for(int i = 0;i <= deviceANum;i++)
            deviceAJComboBox.addItem(i+"");
        for(int i = 0;i <= deviceBNum;i++)
            deviceBJComboBox.addItem(i+"");
        

    }
